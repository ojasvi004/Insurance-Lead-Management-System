package DataObjects;

public class LeadContactDO {
	String leadContactSeq;
	String contactType;
	String contactNum;
	
	public String getLeadContactSeq() {
		return leadContactSeq;
	}
	public void setLeadContactSeq(String leadContactSeq) {
		this.leadContactSeq = leadContactSeq;
	}
	public String getContactType() {
		return contactType;
	}
	public void setContactType(String contactType) {
		this.contactType = contactType;
	}
	public String getContactNum() {
		return contactNum;
	}
	public void setContactNum(String contactNum) {
		this.contactNum = contactNum;
	}
}
